package java_week2;
//Write a Java program to swap two variables.

public class Que_15 {//class Que_15

    public static void main(String[] args) {//main method
        int a = 10;
        int b = 15;

        System.out.println("Before swap: a,b= "+a+"," + + b);
        a= a+b;
        b=a-b;
        a=a-b;
        System.out.println("After swap: a,b = "+a+","+ + b);
    }
    }

